// Command gen produces onboardctl's shell completions (bash / zsh / fish)
// and a roff manpage, writing them under a caller-supplied output
// directory.
//
// Usage:  gen <output-dir>
//
// Invoked twice:
//   - make extras → developers want the files locally
//   - .goreleaser.yaml before.hooks → release archives bundle them
//
// This is a tiny tool, not a library, so it's allowed to Fatal on any
// error — a broken run of gen should fail the surrounding build loud.
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"time"

	"github.com/spf13/cobra/doc"

	"github.com/ZlatanOmerovic/onboardctl/internal/cli"
)

func main() {
	if len(os.Args) != 2 {
		fmt.Fprintln(os.Stderr, "usage: gen <output-dir>")
		os.Exit(2)
	}
	outDir := os.Args[1]

	root := cli.Root()
	// Suppress the "Auto generated by spf13/cobra on <date>" footer so
	// generated files are reproducible across runs.
	root.DisableAutoGenTag = true

	compDir := filepath.Join(outDir, "completions")
	manDir := filepath.Join(outDir, "man")
	must(os.MkdirAll(compDir, 0o755))
	must(os.MkdirAll(manDir, 0o755))

	// ---- completions ----
	writeCompletion := func(filename string, gen func(*os.File) error) {
		path := filepath.Join(compDir, filename)
		f, err := os.Create(path)
		must(err)
		defer f.Close()
		must(gen(f))
		fmt.Printf("wrote %s\n", path)
	}

	writeCompletion("onboardctl.bash", func(f *os.File) error {
		return root.GenBashCompletion(f)
	})
	writeCompletion("_onboardctl", func(f *os.File) error {
		// zsh convention: completion file starts with an underscore.
		return root.GenZshCompletion(f)
	})
	writeCompletion("onboardctl.fish", func(f *os.File) error {
		return root.GenFishCompletion(f, true)
	})

	// ---- manpage ----
	now := time.Now()
	header := &doc.GenManHeader{
		Title:   "ONBOARDCTL",
		Section: "1",
		Date:    &now,
		Source:  "onboardctl",
		Manual:  "onboardctl Manual",
	}
	must(doc.GenManTree(root, header, manDir))
	fmt.Printf("wrote manpages under %s/\n", manDir)
}

func must(err error) {
	if err != nil {
		fmt.Fprintln(os.Stderr, "gen:", err)
		os.Exit(1)
	}
}
